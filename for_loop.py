# perulangan (loop)

# for kondisi:
#     aksi

# dengan list
pin = [8,1,3,1,5] #ini adalah list
print(pin)

for i in pin:
    print(f"i sekarang ->{i}")
print("akhir dari program 1\n")

# dengan range
pin_range = range(1,11)

for i in pin_range:
    print(f"i sekarang -> {i}")
    
print("akhir dari program 2\n")
    
    
# menggunakan string
data_str = "stay strong brother"
for huruf in data_str:
    print(huruf)
    
print("akhir dari program 3\n")
    